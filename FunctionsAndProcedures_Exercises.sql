DELIMITER $$
CREATE FUNCTION ufn_calculate_future_value
		(initial_sum DECIMAL(10,4),yeary_iRate DOUBLE, years INT)
        RETURNS DECIMAL(16,4)
DETERMINISTIC
BEGIN
RETURN initial_sum * (POW((1 + yeary_iRate),years));
END$$
