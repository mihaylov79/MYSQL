-- 4 Employees from Town
DELIMITER $$
CREATE PROCEDURE usp_get_employees_from_town(town_name VARCHAR(50))
BEGIN
    SELECT first_name, last_name
        FROM employees e
    JOIN addresses a ON a.address_id = e.address_id
    JOIN towns t ON a.town_id = t.town_id
    WHERE t.name = town_name
    ORDER BY  first_name, last_name, employee_id;
END $$
DELIMITER ;

CALL usp_get_employees_from_town('Sofia');



-- 5 Salary Level Function
DELIMITER $$
CREATE FUNCTION ufn_get_salary_level(salary DECIMAL(10,4)) RETURNS VARCHAR(50)
DETERMINISTIC
BEGIN
    DECLARE salary_Level VARCHAR(50);
    CASE
        WHEN salary < 30000 THEN SET @salary_Level := 'Low';
        WHEN salary BETWEEN 30000 AND 50000 THEN SET @salary_Level := 'Average';
        ELSE SET salary_Level :='High';
    END CASE;
    RETURN @salary_Level;
END $$
DELIMITER ;

SELECT ufn_get_salary_level(51000);

