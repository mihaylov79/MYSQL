-- 14 Money Transfer

DELIMITER $$
CREATE PROCEDURE usp_transfer_money
				(from_account_id INT,to_account_id INT, money_amount DECIMAL (16,4))
BEGIN
START TRANSACTION;
IF(SELECT COUNT(*) FROM accounts WHERE id = from_account_id)<> 1 
    OR (SELECT COUNT(*) FROM accounts WHERE id = to_account_id)<> 1
    OR from_account_id = to_account_id
	OR ( (SELECT balance FROM accounts WHERE id = from_account_id) < money_amount)
	OR money_amount < 0 THEN
ROLLBACK;
ELSE
	UPDATE accounts SET balance = balance - money_amount 
		WHERE id = from_account_id;
    UPDATE accounts SET balance = balance + money_amount 
		WHERE id = to_account_id;    
        COMMIT;
END IF;        
END$$
DELIMITER ;

-- 15 Log Accounts Trigger

CREATE TABLE logs(
log_id INT PRIMARY KEY AUTO_INCREMENT,
account_id INT,
old_sum DECIMAL (16,4),
new_sum DECIMAL (16,4)
);

CREATE TRIGGER log_data
AFTER UPDATE
ON accounts FOR EACH ROW
INSERT INTO logs (account_id,old_sum, new_sum) VALUE (
old.id,old.balance, new.balance
);

SELECT * FROM logs;

