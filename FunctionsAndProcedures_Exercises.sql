CREATE PROCEDURE usp_withdraw_money
				(account_id INT, money_amount DECIMAL (16,4))
BEGIN
START TRANSACTION;
IF(SELECT COUNT(*) FROM accounts WHERE accounts.id = account_id)<> 1 
	OR ( (SELECT balance FROM accounts WHERE accounts.id = account_id) < money_amount)
	OR money_amount < 0 THEN
ROLLBACK;
ELSE
	UPDATE accounts
		SET balance = balance - money_amount
        WHERE accounts.id = account_id;
        COMMIT;
